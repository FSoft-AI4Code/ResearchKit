class ResearchAgent(SubAgent):
    name = "Research Agent"
    description = "Deep literature discovery with citation verification and BibTeX output"

    def __init__(self, config: ProviderConfig | None = None):
        self.config = config
        self.provider = create_provider(config) if config else None
        self.max_tool_iterations = max(config.max_tool_iterations, 1) if config else 8
        self.tool_output_max_chars = max(config.tool_output_max_chars, 200) if config else 12000

    async def execute(
        self,
        task: Task,
        memory: PaperMemory | None,
        context: SubAgentContext | None = None,
    ) -> Result:
        logger.info(
            "ResearchAgent execute start mode=%s task_type=%s has_memory=%s has_context=%s task=%r",
            "react" if self.provider is not None else "single_pass",
            task.type,
            memory is not None,
            context is not None,
            self._truncate_text(task.description.strip(), 240),
        )
        if self.provider is None:
            result = await self._execute_single_pass(task, memory)
            logger.info(
                "ResearchAgent execute completed mode=single_pass status=%s artifacts=%d content_chars=%d",
                result.status,
                len(result.artifacts),
                len(result.content),
            )
            return result

        try:
            result = await self._execute_react_loop(task, memory, context)
            logger.info(
                "ResearchAgent execute completed mode=react status=%s artifacts=%d content_chars=%d",
                result.status,
                len(result.artifacts),
                len(result.content),
            )
            return result
        except Exception as exc:  # noqa: BLE001
            logger.exception("ResearchAgent ReAct loop failed: %s", exc)
            fallback = await self._execute_single_pass(task, memory)
            fallback.content += (
                "\n\n- ReAct loop failed and the agent returned a deterministic fallback summary."
            )
            logger.info(
                "ResearchAgent execute completed mode=react_fallback status=%s artifacts=%d content_chars=%d",
                fallback.status,
                len(fallback.artifacts),
                len(fallback.content),
            )
            return fallback

    async def _execute_react_loop(
        self,
        task: Task,
        memory: PaperMemory | None,
        context: SubAgentContext | None,
    ) -> Result:
        state = {
            "artifacts": [],
            "latest_search_bibtex": "",
        }
        logger.info(
            "ResearchAgent ReAct start max_iterations=%d has_workspace_context=%s",
            self.max_tool_iterations,
            bool(context and context.workspace_path),
        )
        messages = [
            {"role": "system", "content": RESEARCH_AGENT_SYSTEM_PROMPT},
            {
                "role": "user",
                "content": self._build_user_message(task, memory, context),
            },
        ]

        for iteration in range(self.max_tool_iterations):
            logger.info(
                "ResearchAgent ReAct iteration start iteration=%d/%d messages=%d artifacts=%d",
                iteration + 1,
                self.max_tool_iterations,
                len(messages),
                len(state["artifacts"]),
            )
            tool_response = await self.provider.complete_with_tools(messages, RESEARCH_AGENT_TOOLS)
            assistant_content = str(tool_response.get("content") or "").strip()
            tool_calls = tool_response.get("tool_calls") or []
            logger.info(
                "ResearchAgent ReAct iteration received iteration=%d tool_calls=%d assistant_chars=%d",
                iteration + 1,
                len(tool_calls),
                len(assistant_content),
            )
            if assistant_content:
                logger.info(
                    "ResearchAgent ReAct assistant content iteration=%d content=%s",
                    iteration + 1,
                    self._truncate(assistant_content, 2000),
                )
            assistant_record = assistant_content or (
                f"Calling {len(tool_calls)} research tool"
                f"{'' if len(tool_calls) == 1 else 's'}."
                if tool_calls
                else ""
            )
            if assistant_record:
                messages.append({"role": "assistant", "content": assistant_record})

            if not tool_calls:
                summary = assistant_content or self._build_fallback_summary(
                    task, state["artifacts"]
                )
                logger.info(
                    "ResearchAgent ReAct completed iteration=%d reason=no_tool_calls artifacts=%d summary_chars=%d",
                    iteration + 1,
                    len(state["artifacts"]),
                    len(summary),
                )
                return Result(
                    status="completed",
                    content=self._format_final_content(summary),
                    artifacts=list(state["artifacts"]),
                )

            for index, tool_call in enumerate(tool_calls, start=1):
                func_name = str(tool_call.get("function", {}).get("name") or "").strip()
                raw_args = tool_call.get("function", {}).get("arguments", "{}")
                logger.info(
                    "ResearchAgent tool call start iteration=%d call=%d/%d tool=%s args=%s",
                    iteration + 1,
                    index,
                    len(tool_calls),
                    func_name,
                    self._truncate(str(raw_args), 1200),
                )
                try:
                    args = json.loads(raw_args) if isinstance(raw_args, str) else dict(raw_args)
                except (TypeError, ValueError, json.JSONDecodeError):
                    error_text = (
                        f"Tool call `{func_name}` has invalid JSON arguments: {raw_args}"
                    )
                    logger.warning(
                        "ResearchAgent tool call invalid arguments iteration=%d tool=%s raw_args=%s",
                        iteration + 1,
                        func_name,
                        self._truncate(str(raw_args), 1200),
                    )
                    messages.append(
                        {
                            "role": "user",
                            "content": self._build_tool_result_message(
                                func_name=func_name,
                                tool_result={"error": error_text},
                                visible_text=error_text,
                            ),
                        }
                    )
                    continue

                execution = await self._execute_react_tool(
                    func_name=func_name,
                    args=args,
                    task=task,
                    memory=memory,
                    context=context,
                    state=state,
                )
                logger.info(
                    "ResearchAgent tool call completed iteration=%d call=%d/%d tool=%s error=%s finished=%s visible_chars=%d artifacts=%d",
                    iteration + 1,
                    index,
                    len(tool_calls),
                    func_name,
                    bool(execution["tool_result"].get("error")),
                    execution["finished_content"] is not None,
                    len(execution["visible_text"]),
                    len(state["artifacts"]),
                )
                logger.info(
                    "ResearchAgent tool call result iteration=%d call=%d/%d tool=%s tool_result=%s",
                    iteration + 1,
                    index,
                    len(tool_calls),
                    func_name,
                    self._truncate(self._safe_json_dumps(execution["tool_result"]), 4000),
                )
                if execution["visible_text"]:
                    logger.info(
                        "ResearchAgent tool call visible output iteration=%d call=%d/%d tool=%s output=%s",
                        iteration + 1,
                        index,
                        len(tool_calls),
                        func_name,
                        self._truncate(execution["visible_text"], 2000),
                    )
                messages.append(
                    {
                        "role": "user",
                        "content": self._build_tool_result_message(
                            func_name=func_name,
                            tool_result=execution["tool_result"],
                            visible_text=execution["visible_text"],
                        ),
                    }
                )
                if execution["finished_content"] is not None:
                    logger.info(
                        "ResearchAgent ReAct finished via tool iteration=%d tool=%s summary_chars=%d",
                        iteration + 1,
                        func_name,
                        len(execution["finished_content"]),
                    )
                    return Result(
                        status="completed",
                        content=self._format_final_content(execution["finished_content"]),
                        artifacts=list(state["artifacts"]),
                    )

        logger.info(
            "ResearchAgent ReAct exhausted max_iterations=%d artifacts=%d",
            self.max_tool_iterations,
            len(state["artifacts"]),
        )
        return Result(
            status="completed",
            content=self._format_final_content(
                self._build_fallback_summary(task, state["artifacts"], exhausted=True)
            ),
            artifacts=list(state["artifacts"]),
        )

    async def _execute_react_tool(
        self,
        *,
        func_name: str,
        args: dict,
        task: Task,
        memory: PaperMemory | None,
        context: SubAgentContext | None,
        state: dict,
    ) -> dict:
        if func_name == "search_literature":
            query = str(args.get("query") or "").strip() or self._build_search_query(
                task.description, memory
            )
            focus = str(args.get("focus") or "").strip()
            logger.info(
                "ResearchAgent search_literature requested query=%r focus=%r",
                query,
                self._truncate_text(focus, 160) if focus else "",
            )
            if not query:
                message = "search_literature requires a non-empty query."
                return self._tool_error(message)

            search_description = task.description.strip()
            if focus:
                search_description += f"\n\nFocus: {focus}"

            asta_api_key = (self.config.asta_api_key if self.config else "") or ""
            asta_mcp_url = (self.config.asta_mcp_url if self.config else "") or ""
            try:
                papers, snippets = await self._search_with_asta(
                    query,
                    search_description,
                    memory,
                    asta_api_key=asta_api_key,
                    asta_mcp_url=asta_mcp_url,
                )
            except Exception as exc:  # noqa: BLE001
                logger.warning("Research search failed for query=%r: %s", query, exc)
                return self._tool_error(f"Search failed for query `{query}`: {exc}")

            top_papers = papers[:10]
            bibtex_text = papers_to_bibtex(top_papers) if top_papers else ""
            artifact = {
                "type": "literature_search_result",
                "query": query,
                "result_count": len(papers),
                "papers": [paper.to_dict() for paper in top_papers],
                "bibtex": bibtex_text,
            }
            state["artifacts"].append(artifact)
            state["latest_search_bibtex"] = bibtex_text
            logger.info(
                "ResearchAgent search_literature completed query=%r papers=%d snippets=%d bibtex_chars=%d",
                query,
                len(papers),
                len(snippets),
                len(bibtex_text),
            )
            return {
                "tool_result": {
                    "query": query,
                    "result_count": len(papers),
                    "papers": [self._paper_preview(paper) for paper in top_papers[:5]],
                    "snippets": [self._snippet_preview(snippet) for snippet in snippets[:3]],
                    "bibtex": bibtex_text,
                    "artifacts": [artifact],
                },
                "visible_text": self._summarize_search_artifact(artifact, snippets),
                "finished_content": None,
            }

        if func_name == "verify_citations":
            bibtex = str(args.get("bibtex") or "").strip()
            use_latest_search_bibtex = bool(args.get("use_latest_search_bibtex"))
            if use_latest_search_bibtex and not bibtex:
                bibtex = str(state.get("latest_search_bibtex") or "").strip()
            logger.info(
                "ResearchAgent verify_citations requested use_latest_search_bibtex=%s bibtex_chars=%d",
                use_latest_search_bibtex,
                len(bibtex),
            )
            if not bibtex:
                message = "Verification skipped: no BibTeX entries were provided or generated."
                return self._tool_error(message)

            try:
                report = verify_citations(bibtex)
            except Exception as exc:  # noqa: BLE001
                logger.warning("Citation verification failed: %s", exc)
                return self._tool_error(f"Citation verification failed: {exc}")

            report_dict = report.to_dict()
            summary = report_dict.get("summary", {})
            artifact = {
                "type": "citation_verification_result",
                "summary": summary,
                "results": report_dict.get("results", []),
            }
            state["artifacts"].append(artifact)
            logger.info(
                "ResearchAgent verify_citations completed verified=%s suspicious=%s hallucinated=%s skipped=%s",
                summary.get("verified", 0),
                summary.get("suspicious", 0),
                summary.get("hallucinated", 0),
                summary.get("skipped", 0),
            )
            return {
                "tool_result": {
                    "summary": summary,
                    "flagged": [
                        item
                        for item in report_dict.get("results", [])
                        if isinstance(item, dict)
                        and item.get("status") in {"suspicious", "hallucinated"}
                    ][:5],
                    "artifacts": [artifact],
                },
                "visible_text": self._summarize_verification_artifact(artifact),
                "finished_content": None,
            }

        if func_name == "str_replace_editor":
            logger.info(
                "ResearchAgent str_replace_editor requested command=%s path=%s view_range=%s",
                str(args.get("command") or "").strip(),
                str(args.get("path") or "").strip(),
                args.get("view_range"),
            )
            try:
                tool_result = self._execute_read_only_editor(
                    context=context,
                    command=str(args.get("command") or "").strip(),
                    path=str(args.get("path") or "").strip(),
                    view_range=args.get("view_range"),
                )
            except Exception as exc:  # noqa: BLE001
                return self._tool_error(str(exc))
            logger.info(
                "ResearchAgent str_replace_editor completed path=%s status=%s preview_chars=%d",
                tool_result.get("path"),
                tool_result.get("status"),
                len(str(tool_result.get("preview") or "")),
            )
            return {
                "tool_result": tool_result,
                "visible_text": str(tool_result.get("preview") or ""),
                "finished_content": None,
            }

        if func_name == "finish":
            summary = str(args.get("summary") or "").strip()
            logger.info(
                "ResearchAgent finish requested summary_chars=%d",
                len(summary),
            )
            return {
                "tool_result": {"status": "completed"},
                "visible_text": summary,
                "finished_content": summary or self._build_fallback_summary(
                    task, state["artifacts"]
                ),
            }

        return self._tool_error(f"Unsupported research tool: {func_name}")

    async def _execute_single_pass(self, task: Task, memory: PaperMemory | None) -> Result:
        description = task.description.strip()
        query = self._build_search_query(description, memory)
        bibtex_block = self._extract_bibtex(description)
        verify_intent = self._is_verification_request(description)
        should_search = self._should_run_search(description, bibtex_block)
        should_verify = verify_intent or bool(bibtex_block)

        artifacts: list[dict] = []
        lines = ["### Research Agent"]
        logger.info(
            "ResearchAgent single-pass start query=%r should_search=%s should_verify=%s has_bibtex=%s",
            query,
            should_search,
            should_verify,
            bool(bibtex_block),
        )

        asta_api_key = (self.config.asta_api_key if self.config else "") or ""
        asta_mcp_url = (self.config.asta_mcp_url if self.config else "") or ""

        searched_papers: list[Paper] = []
        if should_search and query:
            logger.info("ResearchAgent single-pass search start query=%r", query)
            try:
                searched_papers, snippets = await self._search_with_asta(
                    query,
                    description,
                    memory,
                    asta_api_key=asta_api_key,
                    asta_mcp_url=asta_mcp_url,
                )
            except Exception as exc:  # noqa: BLE001
                logger.warning("Research search failed for query=%r: %s", query, exc)
                lines.append(f"- Search failed for query `{query}`: {exc}")
            else:
                top_papers = searched_papers[:10]
                bibtex_text = papers_to_bibtex(top_papers) if top_papers else ""
                artifact = {
                    "type": "literature_search_result",
                    "query": query,
                    "result_count": len(searched_papers),
                    "papers": [paper.to_dict() for paper in top_papers],
                    "bibtex": bibtex_text,
                }
                artifacts.append(artifact)
                logger.info(
                    "ResearchAgent single-pass search completed query=%r papers=%d snippets=%d bibtex_chars=%d",
                    query,
                    len(searched_papers),
                    len(snippets),
                    len(bibtex_text),
                )
                lines.append(self._summarize_search_artifact(artifact, snippets))

        bibtex_for_verify = bibtex_block
        if should_verify and not bibtex_for_verify and searched_papers:
            bibtex_for_verify = papers_to_bibtex(searched_papers[:20])

        if should_verify:
            if not bibtex_for_verify:
                logger.info("ResearchAgent single-pass verification skipped reason=no_bibtex")
                lines.append("- Verification skipped: no BibTeX entries were provided or generated.")
            else:
                logger.info(
                    "ResearchAgent single-pass verification start bibtex_chars=%d",
                    len(bibtex_for_verify),
                )
                try:
                    report = verify_citations(bibtex_for_verify)
                except Exception as exc:  # noqa: BLE001
                    logger.warning("Citation verification failed: %s", exc)
                    lines.append(f"- Citation verification failed: {exc}")
                else:
                    report_dict = report.to_dict()
                    artifact = {
                        "type": "citation_verification_result",
                        "summary": report_dict.get("summary", {}),
                        "results": report_dict.get("results", []),
                    }
                    artifacts.append(artifact)
                    logger.info(
                        "ResearchAgent single-pass verification completed verified=%s suspicious=%s hallucinated=%s skipped=%s",
                        artifact["summary"].get("verified", 0),
                        artifact["summary"].get("suspicious", 0),
                        artifact["summary"].get("hallucinated", 0),
                        artifact["summary"].get("skipped", 0),
                    )
                    lines.append(self._summarize_verification_artifact(artifact))

        if len(lines) == 1:
            lines.append("- No actionable literature step could be derived from this request.")

        logger.info(
            "ResearchAgent single-pass completed artifacts=%d content_lines=%d",
            len(artifacts),
            len(lines),
        )
        return Result(
            status="completed",
            content="\n".join(lines),
            artifacts=artifacts,
        )

    def _tool_error(self, message: str) -> dict:
        return {
            "tool_result": {"error": message},
            "visible_text": message,
            "finished_content": None,
        }

    def _build_user_message(
        self,
        task: Task,
        memory: PaperMemory | None,
        context: SubAgentContext | None,
    ) -> str:
        derived_query = self._build_search_query(task.description, memory)
        bibtex_block = self._extract_bibtex(task.description)
        lines = ["Task:", task.description.strip()]
        if derived_query:
            lines.append(f"Derived search query: {derived_query}")
        if bibtex_block:
            lines.append("Request contains BibTeX that may need verification.")
        if context and context.workspace_path:
            lines.extend(
                build_workspace_context_lines(
                    context.workspace_path,
                    active_file=context.file_path,
                )
            )
        if context and context.file_path:
            lines.append(f"Active file: {context.file_path}")
        if context and context.selected_text:
            selection = self._truncate_text(context.selected_text, 300)
            lines.append(f"Selected text: {selection}")
        if context and context.cursor_line is not None:
            lines.append(f"Cursor line: {context.cursor_line}")
        if memory and memory.paper_summary:
            lines.append(f"Paper summary: {self._truncate_text(memory.paper_summary, 400)}")
        if memory and memory.research_questions:
            lines.append("Research questions:")
            lines.extend(f"- {question}" for question in memory.research_questions[:5])
        if memory and memory.contributions:
            lines.append("Contributions:")
            lines.extend(f"- {contribution}" for contribution in memory.contributions[:5])
        if memory and memory.citations:
            lines.append("Known citations:")
            for citation in memory.citations[:10]:
                venue = f" · {citation.venue}" if citation.venue else ""
                year = f" ({citation.year})" if citation.year else ""
                lines.append(f"- {citation.key}: {citation.title}{year}{venue}")
        if memory and memory.structure_map:
            lines.append("Paper structure:")
            for section in memory.structure_map[:10]:
                lines.append(
                    f"- L{section.level} {section.name} [{section.status}]"
                    + (f" in {section.file_path}" if section.file_path else "")
                )
        return "\n".join(lines)

    def _build_tool_result_message(
        self,
        *,
        func_name: str,
        tool_result: dict,
        visible_text: str,
    ) -> str:
        lines = [f"Tool `{func_name}` result:"]
        if tool_result.get("error"):
            lines.append(f"Error: {tool_result['error']}")
        else:
            serialized = self._safe_json_dumps(tool_result)
            lines.append(
                f"```json\n{self._truncate(serialized, self.tool_output_max_chars)}\n```"
            )
        if visible_text:
            lines.append("Visible output:")
            lines.append(
                f"```text\n{self._truncate(visible_text, self.tool_output_max_chars)}\n```"
            )
        return "\n".join(lines)

    def _format_final_content(self, content: str) -> str:
        stripped = content.strip()
        if not stripped:
            stripped = "- No actionable literature step could be derived from this request."
        if stripped.startswith("### "):
            return stripped
        return f"### Research Agent\n{stripped}"

    def _build_fallback_summary(
        self,
        task: Task,
        artifacts: list[dict],
        *,
        exhausted: bool = False,
    ) -> str:
        lines = ["### Research Agent"]
        if exhausted:
            lines.append("- Reached the research tool iteration limit and returning the best partial summary available.")

        search_artifact = next(
            (
                artifact
                for artifact in reversed(artifacts)
                if isinstance(artifact, dict)
                and artifact.get("type") == "literature_search_result"
            ),
            None,
        )
        verification_artifact = next(
            (
                artifact
                for artifact in reversed(artifacts)
                if isinstance(artifact, dict)
                and artifact.get("type") == "citation_verification_result"
            ),
            None,
        )

        if search_artifact:
            lines.append(self._summarize_search_artifact(search_artifact, []))
        if verification_artifact:
            lines.append(self._summarize_verification_artifact(verification_artifact))
        if len(lines) == 1 or (exhausted and len(lines) == 2):
            lines.append(
                "- No completed literature actions were captured; use a follow-up request to continue the investigation."
            )
            lines.append(f"- Original request: {task.description.strip()}")
        return "\n".join(lines)

    def _execute_read_only_editor(
        self,
        *,
        context: SubAgentContext | None,
        command: str,
        path: str,
        view_range: object,
    ) -> dict:
        workspace_path = context.workspace_path if context else None
        if not workspace_path:
            raise ValueError("Workspace access is unavailable for this request.")
        if command != "view":
            raise ValueError("ResearchAgent only allows `str_replace_editor` with the `view` command.")
        editor = WorkspaceStrReplaceEditor(
            workspace_path=workspace_path,
            history=None,
            max_response_chars=self.tool_output_max_chars,
        )
        result = editor.execute(
            command="view",
            path=path,
            view_range=view_range if isinstance(view_range, list) else None,
        )
        workspace_root = Path(os.path.realpath(workspace_path))
        normalized_path = self._normalize_workspace_editor_path(result.path, workspace_root)
        return {
            "command": result.command,
            "path": normalized_path,
            "status": result.status,
            "summary": result.summary,
            "metadata": result.metadata,
            "preview": result.output,
        }

    @staticmethod
    def _normalize_workspace_editor_path(path: str, workspace_root: Path) -> str:
        real_path = Path(os.path.realpath(path))
        real_root = Path(os.path.realpath(str(workspace_root)))
        if real_path == real_root:
            return "."
        return real_path.relative_to(real_root).as_posix()

    @staticmethod
    def _safe_json_dumps(value: object) -> str:
        try:
            return json.dumps(value, ensure_ascii=False, default=str)
        except (TypeError, ValueError):
            return str(value)

    @staticmethod
    def _truncate(value: str, max_chars: int) -> str:
        if len(value) <= max_chars:
            return value
        return value[: max_chars - 16] + "\n...[truncated]"

