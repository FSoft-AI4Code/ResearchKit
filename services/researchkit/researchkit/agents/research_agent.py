    def _summarize_search_artifact(
        self,
        artifact: dict,
        snippets: list[AstaSnippet] | list[dict],
    ) -> str:
        lines = [
            f"- Found **{artifact.get('result_count', 0)}** papers for query: `{artifact.get('query', '')}`."
        ]
        papers = artifact.get("papers") or []
        if papers:
            lines.append("- Top results:")
            for paper in papers[:5]:
                if isinstance(paper, Paper):
                    title = paper.title
                    year = paper.year or "n/a"
                    venue = paper.venue or "Unknown venue"
                    cites = paper.citation_count
                else:
                    title = str(paper.get("title", "Untitled"))
                    year = paper.get("year") or "n/a"
                    venue = paper.get("venue") or "Unknown venue"
                    cites = paper.get("citation_count")
                lines.append(f"  - {title} ({year}) | {venue} | cites={cites}")
        if snippets:
            lines.append("- Matching snippets:")
            for snippet in snippets[:3]:
                if isinstance(snippet, AstaSnippet):
                    title = snippet.paper_title or snippet.paper_id or "Untitled"
                    text = snippet.text
                else:
                    title = str(snippet.get("paper_title") or snippet.get("paper_id") or "Untitled")
                    text = str(snippet.get("text") or "")
                lines.append(f"  - {title}: {self._truncate_text(text, 220)}")
        if artifact.get("bibtex"):
            lines.append("- Generated BibTeX for top results.")
        return "\n".join(lines)

    @staticmethod
    def _summarize_verification_artifact(artifact: dict) -> str:
        summary = artifact.get("summary", {})
        lines = [
            "- Citation verification:"
            f" verified={summary.get('verified', 0)},"
            f" suspicious={summary.get('suspicious', 0)},"
            f" hallucinated={summary.get('hallucinated', 0)},"
            f" skipped={summary.get('skipped', 0)},"
            f" integrity_score={summary.get('integrity_score', 1.0)}."
        ]
        suspicious = [
            item
            for item in artifact.get("results", [])
            if isinstance(item, dict)
            and item.get("status") in {"suspicious", "hallucinated"}
        ]
        if suspicious:
            lines.append("- Flagged references:")
            for item in suspicious[:5]:
                title = str(item.get("title", "Untitled"))
                status = str(item.get("status", "unknown"))
                details = str(item.get("details", ""))
                lines.append(f"  - [{status}] {title} | {details}")
        return "\n".join(lines)

    def _create_asta_client(self, *, asta_api_key: str, asta_mcp_url: str) -> AstaMcpClient:
        return AstaMcpClient(
            asta_api_key,
            server_url=asta_mcp_url,
        )

    async def _search_with_asta(
        self,
        query: str,
        description: str,
        memory: PaperMemory | None,
        *,
        asta_api_key: str,
        asta_mcp_url: str,
    ) -> tuple[list[Paper], list[AstaSnippet]]:
        if not asta_api_key.strip():
            raise RuntimeError("ASTA API key is not configured.")

        publication_date_range = self._build_publication_date_range(description, memory)
        title_queries = self._extract_title_queries(description, memory)
        author_queries = self._extract_author_queries(description)
        logger.info(
            "ResearchAgent ASTA search start query=%r publication_date_range=%s title_queries=%d author_queries=%d",
            query,
            publication_date_range,
            len(title_queries),
            len(author_queries),
        )

        async with self._create_asta_client(
            asta_api_key=asta_api_key,
            asta_mcp_url=asta_mcp_url,
        ) as asta:
            primary_results = await asta.search_papers_by_relevance(
                query,
                limit=12,
                publication_date_range=publication_date_range,
            )
            logger.info(
                "ResearchAgent ASTA relevance search completed query=%r papers=%d",
                query,
                len(primary_results),
            )

            title_results: list[Paper] = []
            for title in title_queries[:8]:
                title_results.extend(
                    await asta.search_paper_by_title(
                        title,
                        publication_date_range=publication_date_range,
                    )
                )
            logger.info(
                "ResearchAgent ASTA title search completed queries=%d papers=%d",
                min(len(title_queries), 8),
                len(title_results),
            )

            author_results: list[Paper] = []
            for name in author_queries[:2]:
                authors = await asta.search_authors_by_name(name, limit=3)
                for author in authors[:2]:
                    author_id = str(author.get("author_id") or "").strip()
                    if not author_id:
                        continue
                    author_results.extend(
                        await asta.get_author_papers(
                            author_id,
                            limit=6,
                            publication_date_range=publication_date_range,
                        )
                    )
            logger.info(
                "ResearchAgent ASTA author expansion completed queries=%d papers=%d",
                min(len(author_queries), 2),
                len(author_results),
            )

            combined = self._deduplicate_papers(primary_results + title_results + author_results)
            logger.info(
                "ResearchAgent ASTA deduplicated initial results papers=%d",
                len(combined),
            )
            enriched = await self._enrich_papers(asta, combined[:8])
            logger.info(
                "ResearchAgent ASTA enrichment completed requested=%d enriched=%d",
                min(len(combined), 8),
                len(enriched),
            )

            citation_results: list[Paper] = []
            for paper in enriched[:3]:
                citation_results.extend(
                    await asta.get_citations(
                        paper.paper_id,
                        limit=6,
                        publication_date_range=publication_date_range,
                    )
                )
            logger.info(
                "ResearchAgent ASTA citation expansion completed seeds=%d papers=%d",
                min(len(enriched), 3),
                len(citation_results),
            )

            merged = self._deduplicate_papers(enriched + combined[8:] + citation_results)

            snippet_ids = [paper.paper_id for paper in merged[:10] if paper.paper_id]
            snippets = await asta.snippet_search(query, limit=5, paper_ids=snippet_ids or None)
            logger.info(
                "ResearchAgent ASTA snippet search completed candidate_ids=%d snippets=%d merged=%d returned=%d",
                len(snippet_ids),
                len(snippets),
                len(merged),
                len(merged[:20]),
            )

            return merged[:20], snippets

    async def _enrich_papers(self, asta: AstaMcpClient, papers: list[Paper]) -> list[Paper]:
        logger.info("ResearchAgent ASTA enrichment start papers=%d", len(papers))
        enriched: list[Paper] = []
        for paper in papers:
            try:
                detailed = await asta.get_paper(paper.paper_id)
            except Exception as exc:  # noqa: BLE001
                logger.info("ASTA get_paper failed for %s: %s", paper.paper_id, exc)
                detailed = None
            enriched.append(detailed or paper)
        logger.info("ResearchAgent ASTA enrichment finished papers=%d", len(enriched))
        return enriched

    @staticmethod
    def _truncate_text(text: str, limit: int) -> str:
        text = " ".join(text.split())
        if len(text) <= limit:
            return text
        return text[: limit - 3].rstrip() + "..."

    @staticmethod
    def _deduplicate_papers(papers: list[Paper]) -> list[Paper]:
        deduped: list[Paper] = []
        seen: set[str] = set()
        for paper in papers:
            keys = [
                paper.doi.lower().strip() if paper.doi else "",
                paper.arxiv_id.lower().strip() if paper.arxiv_id else "",
                re.sub(r"\W+", "", paper.title.lower()),
            ]
            if any(key and key in seen for key in keys):
                continue
            deduped.append(paper)
            seen.update(key for key in keys if key)
        return deduped

    @staticmethod
    def _build_publication_date_range(text: str, memory: PaperMemory | None) -> str:
        current_date = datetime.now(UTC).date()
        lowered = text.lower()
        years = [int(match) for match in re.findall(r"\b(19\d{2}|20\d{2})\b", text)]
        if len(years) >= 2:
            start = min(years)
            end = max(years)
            return f"{start}:{end}"
        if len(years) == 1:
            year = years[0]
            if any(token in lowered for token in ("since", "after", "from")):
                return f"{year}:"
            return str(year)

        if any(
            token in lowered
            for token in ("recent", "latest", "newest", "state of the art", "sota")
        ):
            return f"{current_date.year - 3}:"

        venue_year = ResearchAgent._extract_memory_year(memory)
        if venue_year is not None:
            return f"{max(venue_year - 3, 2000)}:"

        return f"{current_date.year - 5}:"

    @staticmethod
    def _extract_memory_year(memory: PaperMemory | None) -> int | None:
        if not memory or not memory.venue:
            return None

        raw_year = None
        if isinstance(memory.venue, dict):
            raw_year = memory.venue.get("year")
        else:
            raw_year = getattr(memory.venue, "year", None)

        if raw_year is None:
            return None

        if isinstance(raw_year, int):
            return raw_year

        raw_text = str(raw_year).strip()
        if raw_text.isdigit():
            return int(raw_text)

        return None

    @staticmethod
    def _extract_title_queries(text: str, memory: PaperMemory | None = None) -> list[str]:
        titles: list[str] = []
        for match in re.findall(r'"([^"\n]{12,200})"', text):
            value = match.strip()
            if value:
                titles.append(value)
        for match in re.findall(r"'([^'\n]{12,200})'", text):
            value = match.strip()
            if value:
                titles.append(value)

        explicit = re.search(r"title\s*:\s*(.+)", text, flags=re.IGNORECASE)
        if explicit:
            value = explicit.group(1).strip()
            if value:
                titles.append(value[:240])

        if memory and memory.citations:
            citation_map = {
                citation.key.lower(): citation.title.strip()
                for citation in memory.citations
                if citation.key and citation.title.strip()
            }
            for key in ResearchAgent._extract_citation_keys(text):
                title = citation_map.get(key.lower())
                if title:
                    titles.append(title[:240])

        cleaned: list[str] = []
        seen: set[str] = set()
        for title in titles:
            normalized = " ".join(title.split()).strip(" .,:;")
            if len(normalized) < 12:
                continue
            lowered = normalized.lower()
            if lowered in seen:
                continue
            seen.add(lowered)
            cleaned.append(normalized)
        return cleaned

    @staticmethod
    def _extract_author_queries(text: str) -> list[str]:
        candidates: list[str] = []
        explicit = re.search(r"authors?\s*:\s*([^\n]+)", text, flags=re.IGNORECASE)
        if explicit:
            candidates.extend(re.split(r",| and ", explicit.group(1)))

        by_matches = re.findall(
            r"\bby\s+([A-Z][A-Za-z.\-']+(?:\s+[A-Z][A-Za-z.\-']+){0,3})",
            text,
        )
        candidates.extend(by_matches)

        cleaned: list[str] = []
        seen: set[str] = set()
        for candidate in candidates:
            value = " ".join(candidate.split()).strip(" .,:;")
            if len(value.split()) < 2:
                continue
            lowered = value.lower()
            if lowered in seen:
                continue
            seen.add(lowered)
            cleaned.append(value)
        return cleaned

    @staticmethod
    def _extract_citation_keys(text: str) -> list[str]:
        matches = re.findall(r"\b([A-Za-z][A-Za-z0-9_-]*\d{4}[A-Za-z0-9_-]*)\b", text)
        keys: list[str] = []
        seen: set[str] = set()
        for match in matches:
            lowered = match.lower()
            if lowered in seen:
                continue
            seen.add(lowered)
            keys.append(match)
        return keys

    @staticmethod
    def _extract_bibtex(text: str) -> str:
        fenced = re.search(r"```(?:bibtex)?\s*(.*?)```", text, flags=re.IGNORECASE | re.DOTALL)
        if fenced and "@" in fenced.group(1):
            return fenced.group(1).strip()
        if "@" in text and re.search(r"@\w+\s*{", text):
            return text.strip()
        return ""

    @staticmethod
    def _is_verification_request(text: str) -> bool:
        lowered = text.lower()
        keywords = (
            "verify",
            "hallucinated",
            "citation integrity",
            "fact-check citation",
            "check references",
            "validate references",
        )
        return any(keyword in lowered for keyword in keywords)

    @staticmethod
    def _should_run_search(text: str, bibtex_block: str) -> bool:
        lowered = text.lower()
        search_keywords = (
            "search",
            "find",
            "related work",
            "literature",
            "survey",
            "seed paper",
            "paper discovery",
        )
        verify_only = any(
            phrase in lowered
            for phrase in ("verify", "validate references", "check references")
        )
        if verify_only and bibtex_block and not any(k in lowered for k in search_keywords):
            return False
        return True

    @staticmethod
    def _build_search_query(text: str, memory: PaperMemory | None) -> str:
        stripped = text.strip()
        explicit = re.search(r"query\s*:\s*(.+)", stripped, flags=re.IGNORECASE)
        if explicit:
            return explicit.group(1).strip()[:240]

        title_queries = ResearchAgent._extract_title_queries(stripped, memory)
        if title_queries:
            return " ; ".join(title_queries[:3])[:240]

        without_code = re.sub(r"```.*?```", " ", stripped, flags=re.DOTALL)
        without_bib = re.sub(r"@\w+\s*{[^}]*}", " ", without_code)
        collapsed = " ".join(without_bib.split())
        if collapsed:
            return collapsed[:240]

        if memory:
            if memory.research_questions:
                return memory.research_questions[0][:240]
            if memory.paper_summary:
                return memory.paper_summary[:240]
        return ""
